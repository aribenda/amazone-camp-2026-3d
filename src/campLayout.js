export function feetToWorld(xFt, zFt) {
  return {
    x: (xFt - camp.widthFt / 2) * FEET_TO_UNITS,
    z: (zFt - camp.depthFt / 2) * FEET_TO_UNITS,
  };
}

export function feet(value) {
  return value * FEET_TO_UNITS;
}
